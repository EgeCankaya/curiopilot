@pytest.mark.asyncio
async def test_send_happy_path():
    cfg = EmailConfig(
        smtp_host="smtp.gmail.com",
        smtp_port=587,
        sender_email="sender@gmail.com",
        recipient_email="recipient@test.com",
    )
    mock_server = MagicMock()
    mock_server.__enter__ = MagicMock(return_value=mock_server)
    mock_server.__exit__ = MagicMock(return_value=False)

    with patch("curiopilot.email_digest.smtplib.SMTP", return_value=mock_server):
        await send_briefing_email(
            cfg, "# Briefing\n\nContent here.", "2026-03-26", password_override="test-pw"
        )

    mock_server.starttls.assert_called_once()
    mock_server.login.assert_called_once_with("sender@gmail.com", "test-pw")
    mock_server.send_message.assert_called_once()

    # Check the email message content
    msg = mock_server.send_message.call_args[0][0]
    assert msg["Subject"] == "CurioPilot Briefing -- 2026-03-26"
    assert msg["From"] == "sender@gmail.com"
    assert msg["To"] == "recipient@test.com"


