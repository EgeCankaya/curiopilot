"""Tests for the email digest module."""

from __future__ import annotations

import asyncio
from unittest.mock import MagicMock, patch

import pytest

from curiopilot.config import EmailConfig
from curiopilot.email_digest import render_briefing_html, send_briefing_email


# ── render_briefing_html ─────────────────────────────────────────────────────


def test_render_briefing_html_contains_html_structure():
    md = "# Hello World\n\nSome **bold** text."
    html = render_briefing_html(md)
    assert "<!DOCTYPE html>" in html
    assert "<h1>" in html
    assert "<strong>bold</strong>" in html
    assert "Generated by CurioPilot" in html


def test_render_briefing_html_tables():
    md = "| A | B |\n|---|---|\n| 1 | 2 |"
    html = render_briefing_html(md)
    assert "<table>" in html
    assert "<td>1</td>" in html


